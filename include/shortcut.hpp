//! \brief Interface utilisateur pour les gestions des raccourci clavier globaux.
//!
//! Voici un exemple d'utilisation. Dans cette exemple la classe App hrite de
//! la class wxApp.
//! \code
//! bool App::OnInit()
//! {
//! 	//Cration d'un objet Shortcut.
//! 	_shortcut = new Shortcut(this);	
//! 	
//! 	//Cration d'un raccourci, touche super+a 
//! 	_shortcut->creat(KEY_MODIFIER_SUPER, 'a');
//! 	
//! 	//Cration d'un raccourci, touche super+b
//! 	_shortcut->creat(KEY_MODIFIER_SUPER, 'b');
//!
//! 	//Cration d'un raccourci, touche shift+super+a
//! 	ShortcutKey shortcutKey(ShortcutKey::stringToShortcutKey("shift+super+a"));
//! 	_shortcut->creat(shortcutKey);
//! 	
//! 	//Cration d'un raccourci, touche super+ctrl+a 
//! 	_shortcut->creat(KEY_MODIFIER_SUPER|KEY_MODIFIER_CONTROL, 'a');
//! 
//! 	return true;
//! }
//! 
//! void App::OnShortcut(ShortcutEvent& event)
//! {
//! 	std::cout << "OnShortcut -> " << event.getCharKey() << " : ";
//! 						std::cout << event.getModifiers() << std::endl;		
//! }
//! \endcode
class Shortcut
{
	public:
		//! \brief Constructeur.
		//! \param owner est un wxEvtHandler qui est utiliser pour gnrer les vnements.
		//! \note Par dfaut les raccourcis sont activs. Il n'est donc pas ncessaire d'appeler la mthode \ref enable().
		Shortcut(wxEvtHandler* owner);
		
		//! \brief Destructeur.
		~Shortcut();
		
		//! \brief Cr un nouveau raccourci.
		//! \code
		//! //Cration d'un raccourci, touche "super+ctrl+a" 
		//! 	id = _shortcut->creat(KEY_MODIFIER_SUPER|KEY_MODIFIER_CONTROL), 'a');
		//! \endcode
		//! \param modifiers Le modificateur lier au raccourci, peut tre une combinaison de \ref Modifier_e.
		//! \param key la touche du racourci.
		//! \return un nouveau id lier au raccourci. L'id est gnrer avec wxNewId().
		void creat(Modifier_e modifiers, Key_e key);
		
		//! \brief Cr un nouveau raccourci.
		//! \param shortcutKey Le raccourci.
		//! \return un nouveau id lier au raccourci. L'id est gnrer avec wxNewId().
		void creat(ShortcutKey const& shortcutKey);
		
		//! \brief Supprimer un raccourci.
		//! \param modifiers Le modificateur lier au raccourci, peut tre une combinaison de \ref Modifier_e.
		//! \param key la touche du raccourci.
		void remove(Modifier_e modifiers, Key_e key);
		
		//! \brief Supprimer un raccourci.
		//! \param shortcutKey Le raccourci  supprimer.
		void remove(ShortcutKey const& shortcutKey);
		
		//! \brief Supprimer touts les raccourcis.
		void removeAll();
		
		//! \brief Active ou dsactive les raccourcis.
		//! Par dfaut les raccourcis sont activs.
		//! \param val true pour activer et false pour dsactiver.
		void enable(bool val = true);
		
		//! \brief Pour savoir si les raccourcis son activer ou pas.
		bool isEnable();
		
	private:
		//! \brief Les raccourcis qui on t cre.
		//! \see creat()
		std::vector<ShortcutKey> _shortcutKeys;
		
		ShortcutThread* _thread;
		
		//! \brief utiliser pour gnrer les vnements.
		wxEvtHandler *_owner;
		
		//! \brief Raccourci activer ou pas.
		//! \see enable()
		bool _enable;
};

